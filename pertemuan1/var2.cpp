#include <iostream>
using namespace std;

int main() {
   // Membuat variabel bertipe bilangan bulat (int) dengan nilai awal 3.
   int num = 3;
  
   // Menampilkan nilai num, yaitu 3, lalu endl membuat kursor pindah ke baris baru.
   cout << num << endl;
  
   // Update the value
   num = 7;
  
   // Printing the updated value
   cout << num;
  
   return 0;
}

//endl dipakai saat ingin output berikutnya tampil di baris baru.